#include "main.h"

int	rgba(double r, double g, double b, double a)
{
	int	res;

	res = (int)(r * 255 * a) << 16;
	res |= (int)(g * 255 * a) << 8;
	res |= (int)(b * 255 * a);
	return (res);
}
